package Btk_7_Cal;

public class DortIslem {

    public double Topla(double sayi1, double sayi2) {
        return sayi1 + sayi2;
    }

    public int Cikar(int sayi1, int sayi2) {
        return sayi1 - sayi2;
    }

    public int Carp(int sayi1, int sayi2) {
        return sayi1 * sayi2;
    }

    public double Bol(double sayi1, double sayi2) {
        return sayi1 / sayi2;
    }


}
